radius=prompt('What is the value of your circle radius:')
area=Math.PI*Math.pow(radius, 2);
alert(`The area of the circle is ${area}`)

value=prompt('What is the value you want to calculate its square root:')
sqrt=Math.sqrt(value)
alert(`The square root of ${value} is ${sqrt}`)

angle=prompt('Enter the angle:')
itscos=Math.cos(angle)
document.write(`cos ${angle} is ${itscos}`)